import java.util.*;

class Dog{
    String name;
    int age;

    public Dog(String name, int age){
        this.name=name;
        this.age=age;
    }

    public void setName(){
        this.name=name;
    }

    public void setAge(){
        this.age=age;
    }

    public String getName(){
		return name;
	}
	public int getAge(){
		return age;
	}
}
